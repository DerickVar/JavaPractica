    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         try {
        int numero = Integer.parseInt(javax.swing.JOptionPane.showInputDialog(this, "Ingrese un número para eliminar sus múltiplos:"));

        int[][] matriz = Utilidades.stringToMatriz(textArea1.getText());
        matriz = Utilidades.eliminarMultiplos(matriz, numero);
        
        textArea1.setText(Utilidades.matrizToString(matriz));

    } catch (NumberFormatException ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Ingrese un número válido.");
    }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         try {
        int filas = Integer.parseInt(jTextField1.getText().trim());
        int columnas = Integer.parseInt(jTextField2.getText().trim());

        if (filas <= 0 || columnas <= 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Las filas y columnas deben ser mayores a 0.");
            return;
        }

        int[][] matriz = Utilidades.generarMatriz(filas, columnas);
        textArea1.setText(Utilidades.matrizToString(matriz));

    } catch (NumberFormatException ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Ingrese valores válidos para filas y columnas.");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

